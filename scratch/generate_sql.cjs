let wordsRaw = rawText.split('\n')
  .map(line => line.trim())
  .filter(line => line.includes(':'))
  .map(line => line.split(':')[0].trim());

// Remove duplicates but keep order
let words = [...new Set(wordsRaw)];

let sqlValues = [];

for (let w of words) {
  let length = w.length;
  let tags = [];
  
  // کلاسیک: تەنێ پەیڤێن ٢ بۆ ٥ پیت
  if (length >= 2 && length <= 5) {
      tags.push('classic');
  }
  
  // پەیڤێن دژوار: پەیڤێن ٦ پیت و پتر
  if (length >= 6) {
      tags.push('hard_words');
  }
  
  // تایا پەیڤان: پەیڤێن ڕێک ٥ پیت
  if (length === 5) {
      tags.push('word_fever');
  }
  
  // پەیڤا نهێنی: پەیڤێن ٢ پیت و پتر
  if (length >= 2) {
      tags.push('secret_word');
  }
  
  // هەڤڕکی: پەیڤێن ڕێک ٥ پیت
  if (length === 5) {
      tags.push('battle');
  }

  let tagsStr = tags.map(t => `'${t}'`).join(', ');
  // Use the standard Kurdish 'ھ' for the category string as confirmed by user
  sqlValues.push(`  ('${w}', 'وەسف(ھەڤالناڤ)', 'وەسف(ھەڤالناڤ)', ARRAY[${tagsStr}]::text[])`);
}

let sql = `-- Insert new Adjectives (وەسف/ھەڤالناڤ) into the words table
INSERT INTO public.words (word, hint, category, mode_tags)
VALUES
${sqlValues.join(',\n')}
ON CONFLICT (word, hint) DO NOTHING;
`;

fs.writeFileSync('d:\\Peyivcin App\\add_adjective_words_new.sql', sql, 'utf-8');
console.log('Successfully wrote', words.length, 'words to add_adjective_words_new.sql');
