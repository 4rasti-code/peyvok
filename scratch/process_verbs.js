const uniqueVerbs = [...new Set(verbs)];

const processedVerbs = uniqueVerbs.map(v => ({
    word: v,
    hint: "کار",
    category: "کار"
}));

const classic = processedVerbs.filter(v => v.word.length >= 2 && v.word.length <= 5);
const hard = processedVerbs.filter(v => v.word.length >= 6);
const fever = processedVerbs.filter(v => v.word.length === 5);
const secret = processedVerbs.filter(v => v.word.length >= 2);
