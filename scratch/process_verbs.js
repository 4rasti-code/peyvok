// Generate SQL for 'words' table (matching sync_words.cjs)
let sql = "INSERT INTO words (word, hint, category, mode_tags) VALUES\n";
const sqlRows = processedVerbs.map(v => {
    const modes = [];
    const len = v.word.length;
    if (len >= 2 && len <= 5) modes.push('classic');
    if (len >= 6) modes.push('hard_words');
    if (len === 5) {
        modes.push('word_fever');
        modes.push('multiplayer');
    }
    if (len >= 2) modes.push('secret_word');
    
    // Escape single quotes for SQL
    const escapedWord = v.word.replace(/'/g, "''");
    const modesStr = modes.map(m => `'${m}'`).join(',');
    return `('${escapedWord}', 'کار', 'کار', ARRAY[${modesStr}])`;
});
sql += sqlRows.join(",\n") + " ON CONFLICT (word, hint) DO NOTHING;";

